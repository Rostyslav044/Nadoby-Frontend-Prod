  const { data: session } = useSession();
  
  // Получаем данные о станции метро
  const metroData = METRO_CONTENT[metroSlug];
  const stationName = currentLanguage === 'ua' ? metroData?.nameUa : metroData?.nameRu;
  const lang = currentLanguage === 'ua' ? 'ua' : 'ru';
  
  // ========== ФИЛЬТРАЦИЯ КВАРТИР ПО МЕТРО ==========
  const apartments = useMemo(() => {
    if (!allApartments || allApartments.length === 0) return [];
    if (!metroData) return [];
    
    // Фильтруем только Киев
    const kyivVariants = ['київ', 'kiev', 'kyiv', 'киев', 'києві', 'киеве'];
    const kievApartments = allApartments.filter(apt => {
      if (!apt) return false;
      const cityFields = [
        apt.city,
        apt.originalCity,
        apt.region,
        apt.address
      ].filter(Boolean).map(f => f.toLowerCase());
      return cityFields.some(field => 
        kyivVariants.some(variant => field.includes(variant))
      );
    });
    
    // Фильтруем по станции метро
    const stationNames = [metroData.nameUa, metroData.nameRu];
    const filtered = kievApartments.filter(apt => {
      const aptMetro = apt.metro ? apt.metro.toLowerCase().trim() : '';
      if (!aptMetro) return false;
      
      return stationNames.some(name => {
        const nameLower = name.toLowerCase().trim();
        return aptMetro === nameLower || aptMetro.includes(nameLower) || nameLower.includes(aptMetro);
      });
    });
    
    return filtered;
  }, [allApartments, metroData]);
  
  const totalCount = apartments.length;
  
  const itemsPerPage = isMobile ? 6 : isTablet ? 9 : 12;
