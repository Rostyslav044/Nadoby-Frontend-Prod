// Серверный рендеринг
export async function getServerSideProps({ params }) {
  const { district } = params;
  
  console.log('========== getServerSideProps ==========');
  console.log('Район из URL:', district);
  
  try {
    const baseUrl = process.env.NODE_ENV === 'production' 
      ? process.env.NEXT_PUBLIC_API_URL || 'https://nadoby.com.ua' 
      : 'http://localhost:3000';
    
    console.log('Запрашиваем API:', `${baseUrl}/api/v1/apartments/get-all`);
    
    const apartmentsRes = await fetch(`${baseUrl}/api/v1/apartments/get-all`);
    const apartmentsData = await apartmentsRes.json();
    
    console.log('Ответ от API:', apartmentsData ? 'получен' : 'пустой');
    
    let allApartments = [];
    if (Array.isArray(apartmentsData)) {
      allApartments = apartmentsData;
      console.log('Данные в виде массива, количество:', allApartments.length);
    } else if (apartmentsData.apartments && Array.isArray(apartmentsData.apartments)) {
      allApartments = apartmentsData.apartments;
      console.log('Данные в apartments.apartments, количество:', allApartments.length);
    } else if (apartmentsData.data && Array.isArray(apartmentsData.data)) {
      allApartments = apartmentsData.data;
      console.log('Данные в data, количество:', allApartments.length);
    } else {
      console.log('Неизвестная структура данных:', Object.keys(apartmentsData));
    }
    
    return { 
      props: { 
        allApartments: allApartments,
        districtSlug: district
      } 
    };
  } catch (error) {
    console.error('Ошибка при получении данных района:', error);
    return { 
      props: { 
        allApartments: [], 
        districtSlug: district
      } 
    };
  }
}

